package com.example.simpleappcompat;

public class AgentGetter {

	String name;
	String address;
	String phone;
	String fax;
	String email;
	String region;
	String city;
	String state;
	String person;
	String type;
	int status;
	
	public AgentGetter(String name, String address, String phone, String fax,
			String email, String region, String city, String state,
			String person, String type, int status) {
		super();
		this.name = name;
		this.address = address;
		this.phone = phone;
		this.fax = fax;
		this.email = email;
		this.region = region;
		this.city = city;
		this.state = state;
		this.person = person;
		this.type = type;
		this.status = status;
	}
}
